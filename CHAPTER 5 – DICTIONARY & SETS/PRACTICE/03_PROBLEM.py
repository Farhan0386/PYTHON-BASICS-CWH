empty_set=set() # Initialize an empty set
empty_set.add(1)  # Add an integer to the set
empty_set.add("2")  # Add a string to the set
empty_set.add(3)  # Add another integer to the set
empty_set.add("4")  # Add another string to the set

print("here we can see one number is stored as string and the others as integer", empty_set)  # Print the set to show the current state
# This will display all elements in the set, which are unique and unordered